//Bitwise Operator
class Bitwiseoperator3{
        public static void main(String[] args){
                int x = 35;     //0010 0011
                int y = 21;     //0001 0101
		System.out.println(x&y);	//use AND truth table=1
                System.out.println(x|y);        //use OR truth table=55
                }
}

