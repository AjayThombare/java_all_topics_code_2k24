class P7{
	public static void main(String[] args){
		int x = 10;
		System.out.println(+x);		//10
		System.out.println(-x);		//-10
		System.out.println(++x);	//11
		System.out.println(--x);	//10
		System.out.println(x++);	//10
		System.out.println(x--);	//11
		System.out.println(x++ + ++x + ++x + --x);
		System.out.println(x);

	}
}

